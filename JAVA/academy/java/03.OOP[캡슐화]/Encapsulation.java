
public class Encapsulation {
	/* OOP에서 클래스 작성 방법 
	 *   1. 캡슐화(encapsulation)
	 *   2. 상속성
	 *   3. 다형성(오버로딩,오바라이딩,객체형변환)
	 */

	/*
	 * 1. 캡슐화
	 *    - 외부클래스(객체)에서 멤버필드에 접근을 막고
	 *      멤버 메쏘드에만 호출(접근)할수있도록 클래스를 설계하는방법
	 *    
	 *    - 구현 : 멤버필드  의 접근제한자(access modifier)를 private 
	 *             멤버메쏘드의 접근제한자(access modifier)는 public 으로한다.
	 *              
	 *             public  ==> 어떤 외부클래스(객체)에서든지 접근가능
	 *             private ==> 어떤 외부클래스(객체)에서든지 접근불가능
	 */
}

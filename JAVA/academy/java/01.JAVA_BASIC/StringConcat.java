
public class StringConcat {

	public static void main(String[] args) {
		int year =2020;
		int month=12;
		int day = 9;
		
		String msg="오늘은 "+year+"년 "+month+"월 "+day+" 일 입니다";
		System.out.println(msg);
		
		System.out.println("오늘은 "+year+"년 "+month+"월 "+day+" 일 입니다");
		
		System.out.println(1 + 2 + "김");
		System.out.println(1 + (2 + "김"));
		
		
		
		

	}

}
